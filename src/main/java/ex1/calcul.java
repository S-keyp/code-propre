package ex1;

/**
 * Calcul class
 */
public class Calcul {
	/**
	 * Add a + b
	 * @param a int
	 * @param b int
	 * @return int
	 */
	public int add(int a, int b){
		return a+b;
	}
	
	/**
	 * Substract a - b
	 * @param a int
	 * @param b int
	 * @return int
	 */
	public int substract(int a, int b){
		return a-b;
	}
}
